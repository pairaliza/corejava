package com.paira.ifelse;

import java.util.Scanner;

public class Assignment4 {
	 public static void main(String[] args) {
	      

	        Scanner scanner = new Scanner(System.in);
	        int A = scanner.nextInt();
	        int B = scanner.nextInt();

	        if(A >= B){
	            System.out.print(A);

	        }
	        else{
	            System.out.print(B);
	        }
	    }
	

}
