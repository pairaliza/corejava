package com.paira.whileloop1;

import java.util.Scanner;

public class Homework4 {
	public static void main(String[] args) {
		
		  Scanner scanner = new Scanner(System.in);
			int N = scanner.nextInt();

			int i = 1;
			while (i <= 10) {

				System.out.println(N + " * " + i + " = " + N * i);
				i++;
			}
			
	}
}

		
	


